export const DEFAULT_DAISYUI_THEME = 'dark';

export function getThemeByName(themeName) {
  return DAISYUI_THEME_OPTIONS.find((theme) => theme.name === themeName) || DAISYUI_THEME_OPTIONS[1];
}

export function getNavigationTheme(theme) {
  return {
    dark: theme.isDark,
    colors: {
      primary: theme.accent,
      background: theme.background,
      card: theme.card,
      text: theme.text,
      border: theme.border,
      notification: theme.accent,
    },
  };
}

function createTheme(name, label, background, card, text, muted, border, accent, accentSoft, isDark) {
  return {
    name,
    label,
    background,
    card,
    text,
    muted,
    border,
    accent,
    accentSoft,
    isDark,
  };
}
